import Foundation
import UIKit

public class ACMaterialDesignIcons {
    public var iconAttributedString: NSMutableAttributedString?
    public var fontSize: CGFloat = 0.0
    
    public static func loadFont() {
        let bundle = Bundle(for: ACMaterialDesignIcons.self)
        
        let identifier = bundle.bundleIdentifier
        let name = "Material-Design-Iconic-Font"
        
        var fontURL: URL
        if identifier?.hasPrefix("org.cocoapods") == true {
            // If this framework is added using CocoaPods, resources is placed under a subdirectory
            fontURL = bundle.url(forResource: name, withExtension: "ttf", subdirectory: "ACMaterialDesignIcons.bundle")!
        } else {
            fontURL = bundle.url(forResource: name, withExtension: "ttf")!
        }
        
        let fontDataProvider = CGDataProvider(url: fontURL as CFURL)
        let newFont = CGFont(fontDataProvider!)
        
        
        CTFontManagerRegisterGraphicsFont(newFont, nil)
    }
    
    public static func icon(withCode code: String!, fontSize: CGFloat) -> ACMaterialDesignIcons {
        loadFont()
        
        let icon = ACMaterialDesignIcons()
        icon.fontSize = fontSize
        icon.iconAttributedString = NSMutableAttributedString(string: code, attributes: [NSFontAttributeName: iconFont(withSize: fontSize)!])
        
        return icon
    }
    
    public static func iconFont(withSize size: CGFloat) -> UIFont? {
        let font = UIFont(name: "Material-Design-Iconic-Font", size: size)
        
        return font
    }
    
    public func image(_ imageSize: CGSize? = nil) -> UIImage {
        var imageSize = imageSize
        if imageSize == nil {
            imageSize = CGSize(width: fontSize, height: fontSize)
        }
        
        UIGraphicsBeginImageContextWithOptions(imageSize!, false, UIScreen.main.scale)
        
        let iconSize = iconAttributedString?.size()
        let xOffset = (imageSize!.width - iconSize!.width) / 2.0
        let yOffset = (imageSize!.height - iconSize!.height) / 2.0
        
        let rect = CGRect(x: xOffset, y: yOffset, width: iconSize!.width, height: iconSize!.height)
        iconAttributedString?.draw(in: rect)
        
        let iconImage = UIGraphicsGetImageFromCurrentImageContext()
        
        UIGraphicsEndImageContext()
        
        return iconImage!
    }
    
    public func rangeForAttributedText() -> NSRange {
        return NSRange(location: 0, length: iconAttributedString!.length)
    }
    
    public func addAttributes(_ attributes: Dictionary<String, Any>) {
        iconAttributedString?.addAttributes(attributes, range: rangeForAttributedText())
    }
    
    public func addAttribute(withName name: String, value: Any) {
        iconAttributedString?.addAttribute(name, value: value, range: rangeForAttributedText())
    }
    
    public func removeAttribute(withName name: String) {
        iconAttributedString?.removeAttribute(name, range: rangeForAttributedText())
    }
    
